
public class _20_TypeCasting {

	public static void main(String[] args) {
		
		String Str1 = "123";
		String Str2 = "3.14";
		
		float Sayi1;
		double Sayi2;
		
		Sayi1 = Float.valueOf(Str1);
		Sayi2 = Double.valueOf(Str2);
		
		System.out.println(Sayi1);
		System.out.println(Sayi2);

	}

}
