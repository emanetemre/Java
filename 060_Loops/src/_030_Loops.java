public class _030_Loops {

	public static void main(String[] args) {
		
		for(int sayi = 1; sayi < 10; sayi+=1) {
			System.out.print(sayi);
			System.out.print("  ");
			
			System.out.print(sayi * sayi);
			System.out.print("  ");
			
			System.out.print(sayi * sayi * sayi);
			System.out.println("  ");
			
		}
		
	}

}
