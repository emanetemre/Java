
public class _020_Loops {

	public static void main(String[] args) {
		for(int sayi = 5; sayi <= 50; sayi = sayi+5) { // sayiyi 5'er artirarark 50 kadar say
					System.out.println(sayi);
		}
		
		for (int sayi2 = 5; sayi2 <= 50; sayi2+=5) { // sayiyi 5'er artirarark 50 kadar say
			System.out.println(sayi2);
		}
	}

}
