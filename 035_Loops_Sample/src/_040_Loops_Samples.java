public class _040_Loops_Samples {

	public static void main(String[] args) {
		int basla, bitis, adim;
		int toplam = 0;
		
		basla = 10;
		bitis = 70;
		adim = 10;
		for(int sayi = basla; sayi <= bitis; sayi += adim) {
			System.out.println(toplam);
			toplam = sayi + adim;}
		
			System.out.println(toplam);
	}

}
