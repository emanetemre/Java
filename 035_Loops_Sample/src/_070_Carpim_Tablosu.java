public class _070_Carpim_Tablosu {

	public static void main(String[] args) {
		
		for (int carpilan = 1; carpilan <= 10; carpilan++) {
			for (int carpim = 1; carpim <=10; carpim++) {
				System.out.print(carpilan);
				System.out.print(" * ");
				System.out.print(carpim);
				System.out.print(" = ");
				System.out.println(carpilan*carpim);
				
			}
			System.out.println("-----------------------");
		}
	}

}
