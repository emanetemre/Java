
public class _020_Array {

	public static void main(String[] args) {
		
		String[] diller = new String[6];
		
		diller[0] = "Java";
		diller[1] = "C#";
		diller[2] = "Basic";
		diller[3] = "Pascal";
		diller[4] = "JavaScript";
		diller[5] = "Ruby";
				
		
		for(int i = 0; i < diller.length; i++) {
			System.out.println(diller[i].toUpperCase());
		}

	}

}
