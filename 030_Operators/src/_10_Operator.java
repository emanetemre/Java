
public class _10_Operator {

	public static void main(String[] args) {

		int Sayi1 = 12;
		int Sayi2 = 5;
		
		int Toplam;

		Toplam = Sayi1 + Sayi2;
		System.out.println(Toplam);

		System.out.println(Sayi1 + Sayi2); // Topla
		System.out.println(Sayi1 - Sayi2); // Çıkar
		System.out.println(Sayi1 * Sayi2); // Çarpma
		System.out.println(Sayi1 / Sayi2); // Bölme
		System.out.println(Sayi1 % Sayi2); // Modunu alma

	}

}
