
public class _50_Operator {

	public static void main(String[] args) {
	 int a;
	 
	 a = 5;
	 a = a +5;
	 System.out.println(a);
	 
	 a = 5;
	 a += 5; // a = a + 5
	 System.out.println(a);
	 
	 a = 5;
	 a -= 3; // a = a - 5
	 System.out.println(a);
	 
	 a = 5;
	 a *= 4; // a = a * 5
	 System.out.println(a);
	 
	 a = 5;
	 a /= 2; // a = a / 5
	 System.out.println(a);

	}

}
