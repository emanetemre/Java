
public class _40_Operator {

	public static void main(String[] args) {
		int a;
		
		a = 5;
		
		System.out.println(a);
		
		System.out.println(a--);//Değişkenden sonraysa  önce değeri alıyor sonra azaltıyor		
		
		System.out.println(a);
		
		System.out.println("----------------------------");
		
		a = 5;
		System.out.println(a);
		
		System.out.println(--a); //Değişkenden önceyse önce azaltıyor değeri döndürüyor
		
		System.out.println(a);

	}

}
