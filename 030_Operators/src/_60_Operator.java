
public class _60_Operator {

	public static void main(String[] args) {
		
		/*
		 * Java'da iki tam sayının bölümü tam sayı olarak verilir.
		 * Eğer  bölümün kesir kısmını istiyorsak 
		 * işleme giren değişkenlerden birini double veya float olarak tanımlamalıyız
		 */
		int Sayi1 = 10;
		float Sayi2 = 3.0f; // 3 Sayısının küsüratlı yazışı
		
		System.out.println(Sayi1 / Sayi2);

	}

}
