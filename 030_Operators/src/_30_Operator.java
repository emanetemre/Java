
public class _30_Operator {

	public static void main(String[] args) {
		int a;
		
		a = 5;
		
		System.out.println(a);
		
		System.out.println(a++);//Değişkenden sonraysa  önce değeri alıyor sonra artırıyor		
		
		System.out.println(a);
		
		System.out.println("----------------------------");
		
		a = 5;
		System.out.println(a);
		
		System.out.println(++a); //Değişkenden önceyse önce artırıyor değeri döndürüyor
		
		System.out.println(a);
	}

}
